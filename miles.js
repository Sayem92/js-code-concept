function milesToKilometers(miles){
    const kilometers = miles*1.689;
    return kilometers;
}

const miles1 = 5;
const kilometer1 =milesToKilometers(miles1);
console.log('5 miles a total kilometer:',kilometer1);

const miles2 = 10;
const kilometer2 =milesToKilometers(miles2);
console.log('10 miles a total kilometer:',kilometer2);

